package graphsAndTrees;

public class TreeNode {
	
	int value;
	TreeNode left;
	TreeNode right;
	
	public TreeNode(int value) {
		
		this.value = value;
		
	}
	
	public int getValue() {
		return this.value;
	}
	
	public TreeNode getLeft() {
		return this.left;
	}
	
	public TreeNode getRight() {
		return this.right;
	}

}
